; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.
; In this case, the data of interest maps each ASCII character into a picture of the character. 
; By making use of this data to print a string to the monitor, 
; I will begin to learn how to organize data in a way that makes it easy to use in a program.
;R0 point to the string
;R2 represent printed row, starting from 0
;R3 represent column, the number of char in the string being printed
;R4 point to the font to be printed
;R5 count the bit 8
;R6 is temporary register






.ORIG x3000


	ZERO_	.FILL 	x5000
	ONE_	.FILL	x5001
	CHAR	.FILL	x5002


;R2 represent printed row, starting from 0
	AND R2, R2, #0

;R3 represent column, the number of char in the string being printed
	AND R3, R3, #0

;R0 point to the string
;R4 point to the font to be printed
;R6 is temporary register

NEXT_CHAR
	LD	R0, CHAR	
	ADD R0, R0, R3	; point to the line counted
	LDR R0, R0, #0
	BRz AGAIN	; which means has loop though all the char in the string
	ADD R3, R3, #1
	

	LEA	R4, FONT_DATA	
	ADD	R6, R0, #0	;use R6 to store the value to be print

; Find the char to be print, store the address in R4
FND_FNT	
	ADD	R4, R4, #15	
	ADD	R4, R4, #1
	ADD	R6, R6, #-1	
	BRp	FND_FNT		

;Now the R4 point to font to be print
;R5 count the bit 8
;R6, store the ascii to be print

Print_char	
	ADD R4, R4, R2

	AND R5, R5, #0
	ADD R5, R5, #8
	
	LDR R6, R4, #0

; Print the R6
PRINT_LOOP	
	ADD R6, R6, #0
	BRzp ZERO		; check if the first bit is 0
	LDI	R0, ONE_	; print if the first bit is 1
	OUT			
	BRnzp	NEXT_BIT	

ZERO	
	LDI	R0, ZERO_	; print if the first bit is 0
	OUT		

NEXT_BIT	
	ADD	R5, R5, #-1
	BRz	NEXT_CHAR		
	ADD	R6, R6, R6
	BRnzp	PRINT_LOOP		

; Here means having loop through all the char in the string
AGAIN
	ADD R4, R2, #-15
	BRp STOP	; check if line count is 16
	ADD R2, R2, #1	; add row count
	AND R3, R3, #0	; initialize bit count
	LD R0, NEW_LINE
	OUT
	BRnzp NEXT_CHAR


NEW_LINE .FILL xA

STOP
	HALT

